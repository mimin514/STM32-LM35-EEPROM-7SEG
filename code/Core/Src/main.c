void run_adc(void){
// Start ADC conversion
    HAL_ADC_Start(&hadc1);
    // Wait for the end of conversion
    if (HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY) == HAL_OK)
    {
      // Get the ADC value
      adc_value = HAL_ADC_GetValue(&hadc1);
      // Convert ADC value to temperature
     float temp_float = ((float)adc_value) * 5 / 4095 * 100;

        // Convert temperature to integer
        int temperature_int = (int)(temp_float + 0.5);  // Round to the nearest integer

        // Format the temperature as a string
        sprintf(temp_str, "%d\n\r", temperature_int);
      // Transmit the temperature string via UART
      HAL_UART_Transmit(&huart1, (uint8_t*)temp_str, strlen(temp_str), HAL_MAX_DELAY);
    }
    // Stop ADC conversion
    HAL_ADC_Stop(&hadc1);
    // Wait for a while before the next reading
    HAL_Delay(1000);
}

